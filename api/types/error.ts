// ============================================================================
// Error Types - Based on Django Backend ErrorCode Enum
// ============================================================================

export interface ErrorResponse {
  code: number
  title: string
  description: string
  detail?: string
  field?: string
}

export interface ValidationError {
  [field: string]: string[]
}

export interface ApiError {
  error?: ErrorResponse
  errors?: ValidationError
  detail?: string
  message?: string
}

